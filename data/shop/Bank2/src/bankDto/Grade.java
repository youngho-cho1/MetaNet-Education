package bankDto;

public class Grade {
	int grade ;
	int count;
	public Grade(int grade, int count) {
		super();
		this.grade = grade;
		this.count = count;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
